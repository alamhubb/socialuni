export default class Position {
static get absolute(){
      return {
        position: 'absolute'
      }
    }
static get fixed(){
      return {
        position: 'fixed'
      }
    }
static get relative(){
      return {
        position: 'relative'
      }
    }
static get static(){
      return {
        position: 'static'
      }
    }
static get inherit(){
      return {
        position: 'inherit'
      }
    }
}