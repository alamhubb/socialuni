export default class FlexDirection {
    static get row() {
        return {
            flexDirection: 'row'
        }
    }

    static get rowReverse() {
        return {
            flexDirection: 'rowReverse'
        }
    }

    static get column() {
        return {
            flexDirection: 'column'
        }
    }

    static get columnReverse() {
        return {
            flexDirection: 'columnReverse'
        }
    }

    static get initial() {
        return {
            flexDirection: 'initial'
        }
    }

    static get inherit() {
        return {
            flexDirection: 'inherit'
        }
    }
}
